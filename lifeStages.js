function pickPoolByAge(age) {
  if (age <= 0) return BABY;
  if (age <= 15) return CHILD;
  if (age <= 29) return YOUNG;
  if (age <= 44) return MID_EARLY;
  if (age <= 59) return MID_LATE;
  if (age <= 74) return SENIOR;
  if (age <= 89) return ELDER;
  return ANCIENT;
}

/**
 * @param {string} name 이름
 * @param {number} lived 나이(쌓인 블록 수)
 * @returns {string[]} 정확히 5개의 문장
 */
export function buildLifeNarrativeFiveLines(name, lived) {
  const n = name || '이름 없음';
  const eun = krTopicEunNeun(n);
  const a = Math.max(0, Math.floor(lived));
  const pool = pickPoolByAge(a);
  return [
    fillTemplate(pickRandom(pool.opening), n, eun, a),
    fillTemplate(pickRandom(pool.middle1), n, eun, a),
    fillTemplate(pickRandom(pool.middle2), n, eun, a),
    fillTemplate(pickRandom(pool.middle3), n, eun, a),
    fillTemplate(pickRandom(pool.closing), n, eun, a)
  ];
}
