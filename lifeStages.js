/**
 * 엔딩 5줄 서술 풀.
 * 각 연령 구간(0 / 1-15 / 16-29 / 30-44 / 45-59 / 60-74 / 75-89 / 90+)마다
 * opening · middle1 · middle2 · middle3 · closing 다섯 풀에서 무작위 1개씩 뽑아
 * 5줄짜리 배열을 만든다.
 *
 * 치환 규칙:
 *  - {n}   → 이름
 *  - {eun} → 받침에 따른 은/는
 *  - {a}   → 나이(정수)
 *  - 이름({n}/{eun}) 치환은 closing 풀에서만 사용 (opening 또는 closing 중 1곳)
 *
 * 톤: 단정적이지 않게, 부드럽고 따뜻하게.
 */

/** 받침 있음 → 은, 없음 → 는 (한글이 아닐 때는 은) */
function krTopicEunNeun(topic) {
  const ch = topic[topic.length - 1];
  const code = ch.charCodeAt(0);
  if (code < 0xac00 || code > 0xd7a3) return '은';
  const hasBatchim = (code - 0xac00) % 28 !== 0;
  return hasBatchim ? '은' : '는';
}

function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function fillTemplate(s, n, eun, a) {
  return s
    .replace(/\{n\}/g, n)
    .replace(/\{eun\}/g, eun)
    .replace(/\{a\}/g, String(a));
}

